public class ExpressoesComparativas {
    public static void main(String[] args) {
        // == igual a
        // != diferente de
        // > maior que
        // < menor que
        // >= maior ou igual
        // <= menor ou igual



        int valorA = 10;
        int valorB = 20;

        System.out.println(valorA == valorB);
        System.out.println(valorA != valorB);
        System.out.println(valorA > valorB);
        System.out.println(valorA < valorB);
        System.out.println(valorA >= valorB);
        System.out.println(valorA <= valorB);

    }
}
